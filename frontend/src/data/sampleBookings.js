import dayjs from "dayjs";

function parseBookedDate(str) {
  const [start, end] = str.split(" - ");
  const endYear = end.match(/\d{4}$/)[0];

  return {
    startDate: dayjs(`${start} ${endYear}`).format("YYYY-MM-DD"),
    endDate: dayjs(end).format("YYYY-MM-DD"),
  };
}

